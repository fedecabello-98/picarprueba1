    public class BuscarProductoC{
        public BuscarProductoC(){
        }
        public String buscarProducto(String idproducto){
            try{
                Statement st = LlenarCombo.conexion.createStatement();
                ResultSet registroproducto = st.executeQuery("SELECT productos.codigo,productos.descripcion,productos.rango FROM picar_db.productos WHERE productos.codigo='"+idproducto+"';");
                if(registroproducto.next()){
                String traerdescripcion = registroproducto.getString("descripcion");
                int traerrango = registroproducto.getInt("rango");
                jTextField2.setForeground(new Color(0,153,255));
                jLabel5.setForeground(new Color(0,153,255));
                jLabel5.setText(traerdescripcion);
                jTextField3.setEnabled(true);
                if(traerrango!=0){
                jTextField3.setText(String.valueOf(traerrango));
                }else{
                 jTextField3.setText("");
                }
                }else{
                    jTextField2.setForeground(Color.red);
                    jLabel5.setForeground(Color.red);
                    jLabel5.setText("NO ENCONTRADO");
                    jTextField3.setText("");
                    jTextField3.setEnabled(false);
                    //jTextField1.requestFocus();
                }
            } catch (SQLException e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "ERROR: "+e, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            return idproducto;
        }
    }
