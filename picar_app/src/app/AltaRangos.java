    public class ModificarRangoC{
        public ModificarRangoC(){
        }
        private String ultimoMensajeMySQL="";
        public String modificarRango (String idproducto, String rangonuevo){
        String sql = "UPDATE picar_db.productos SET rango="+rangonuevo+" WHERE codigo='"+idproducto+"';";
            try{
                Statement st = LlenarCombo.conexion.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "El rango del producto con el código "+idproducto+" fue actualizado con éxito.", "INFORMACIÓN DEL SISTEMA", JOptionPane.INFORMATION_MESSAGE);
                jTextField2.setForeground(Color.black);
                jLabel5.setForeground(Color.black);
                jTextField1.setText("");
                jTextField2.setText("");
                jLabel5.setText("-");
                jTable1.setRowSorter(null);
                jTable1.removeEditor();
                mostrarProductos();
                DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
                rightRenderer.setHorizontalAlignment(JLabel.LEFT);
                jTable1.getColumnModel().getColumn(0).setCellRenderer(rightRenderer);
                jTable1.getColumnModel().getColumn(1).setCellRenderer(rightRenderer);
                jTable1.getColumnModel().getColumn(2).setCellRenderer(rightRenderer);
                jTextField3.setText("");
                jTextField3.setEnabled(false);
                for(int i=0;i<jTable1.getRowCount();i++){
                    if(jTable1.getValueAt(i,0).toString().equals(idproducto)){
                        jTable1.getSelectionModel().setSelectionInterval (i, i);
                        Rectangle r = jTable1.getCellRect (i, 0, true);
                        jScrollPane1.getViewport().scrollRectToVisible(r);
                    }
                }
            } catch (SQLException e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "ERROR: "+e, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
            return ultimoMensajeMySQL;
    }
    }
